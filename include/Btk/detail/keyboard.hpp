enum class Modifier : uint16_t {
    None   = 0,
    Lshift = 1 << 0,
    Rshift = 1 << 1,
    Lctrl  =  1 << 2,
    Rctrl  =  1 << 3,
    Lalt   =  1 << 4,
    Ralt   =  1 << 5,
    Lgui   =  1 << 6,
    Rgui   =  1 << 7,
    Num    =  1 << 8,
    Caps   =  1 << 9,
    Mode   =  1 << 10,

    Shift = Lshift | Rshift,
    Ctrl = Lctrl | Rctrl,
    Gui  = Lgui | Rgui,
};

